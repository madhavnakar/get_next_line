#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*end_of_s1;

	end_of_s1 = ft_strchr(s1, '\0');
	while (*s2 && n)
	{
		*end_of_s1 = *s2;
		end_of_s1++;
		s2++;
		n--;
	}
	*end_of_s1 = '\0';
	return (s1);
}
