#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*end_of_s1;

	end_of_s1 = ft_strchr(s1, '\0');
	while (*s2)
	{
		*end_of_s1 = *s2;
		end_of_s1++;
		s2++;
	}
	*end_of_s1 = '\0';
	return (s1);
}
