#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int ind;

	ind = 0;
	while (*src)
	{
		dst[ind] = *src;
		src++;
		ind++;
	}
	dst[ind] = '\0';
	return (dst);
}
