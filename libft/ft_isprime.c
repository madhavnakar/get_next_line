#include "libft.h"

int	ft_isprime(int n)
{
	int ind;

	if (n <= 1)
		return (0);
	else if (n == 2 || n == 3)
		return (1);
	else if (n % 2 == 0 || n % 3 == 0)
		return (0);
	ind = 5;
	while (n >= ind * ind)
	{
		if (n % ind == 0 || n % (ind + 2) == 0)
			return (0);
		ind += 6;
	}
	return (1);
}
